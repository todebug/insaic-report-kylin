package com.insaic.kylin.model.kylin.config;

import com.insaic.kylin.model.kylin.init.KylinLoadCube;

import java.io.Serializable;

/**
 * Created by dongyang on 2017/10/12.
 */
public class KylinConfigInfo extends KylinLoadCube implements Serializable {
    private static final long serialVersionUID = 1488949390769834293L;

    private String projectCode;

    private String projectName;

    private String modelCode;

    private String modelName;

    public String getProjectCode() {
        return projectCode;
    }

    public void setProjectCode(String projectCode) {
        this.projectCode = projectCode;
    }

    public String getProjectName() {
        return projectName;
    }

    public void setProjectName(String projectName) {
        this.projectName = projectName;
    }

    public String getModelCode() {
        return modelCode;
    }

    public void setModelCode(String modelCode) {
        this.modelCode = modelCode;
    }

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }
}
